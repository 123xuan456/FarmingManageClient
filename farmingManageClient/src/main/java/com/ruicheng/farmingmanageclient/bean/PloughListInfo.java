package com.ruicheng.farmingmanageclient.bean;

import java.io.Serializable;

public class PloughListInfo implements Serializable{

	private String soilState ;
	private String ploughId ;
	private String ploughCode ;
	private String ploughName ;
	private String stationId ;
	private String ploughPoints ;
	private String stationName ;
	private String ploughArea ;
	private String ploughFarm ;
	
	public String getSoilState() {
		return soilState;
	}
	public void setSoilState(String soilState) {
		this.soilState = soilState;
	}
	public String getPloughId() {
		return ploughId;
	}
	public void setPloughId(String ploughId) {
		this.ploughId = ploughId;
	}
	public String getPloughCode() {
		return ploughCode;
	}
	public void setPloughCode(String ploughCode) {
		this.ploughCode = ploughCode;
	}
	public String getPloughName() {
		return ploughName;
	}
	public void setPloughName(String ploughName) {
		this.ploughName = ploughName;
	}
	public String getStationId() {
		return stationId;
	}
	public void setStationId(String stationId) {
		this.stationId = stationId;
	}
	public String getPloughPoints() {
		return ploughPoints;
	}
	public void setPloughPoints(String ploughPoints) {
		this.ploughPoints = ploughPoints;
	}
	public String getStationName() {
		return stationName;
	}
	public void setStationName(String stationName) {
		this.stationName = stationName;
	}
	public String getPloughArea() {
		return ploughArea;
	}
	public void setPloughArea(String ploughArea) {
		this.ploughArea = ploughArea;
	}
	public String getPloughFarm() {
		return ploughFarm;
	}
	public void setPloughFarm(String ploughFarm) {
		this.ploughFarm = ploughFarm;
	}
	
	
	
}
